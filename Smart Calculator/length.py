def meter_to_km(m):
    km = m/1000
    print(f"The {m}m convert into km is: {km}")
    return km

def km_to_meter(km):
    m = km * 1000
    print(f"The {km}km convert into m is: {m}")
    return m
    
def meter_to_feet(m):
    f = m * 3.28084
    print(f"The {m}m convert into f is: {f}")
    return f

def feet_to_meter(f):
    m = f/ 3.28048
    print(f"The {f}f convert into m is: {m}")
    return m

    